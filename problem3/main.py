class Agregasi:
    def __init__(self,num1,num2):
        self.num1 = num1
        self.num2 = num2

    def Penjumlahan(self):
        return self.num1 + self.num2
    
    def Pengurangan(self):
        return self.num1 - self.num2
    
    def Perkalian(self):
        return self.num1*self.num2
    
    def Pembagian(self):
        return self.num1/self.num2
    

